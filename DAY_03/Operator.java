public class Operator {

    public static void main(String[] args) {
        int a = 10;
        int b = 19;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(++a);
        System.out.println(b--);
    }
}

